def _count_remaining_todos(path: Path) -> int:
    """Return the count of unresolved TODO/PLACEHOLDER markers in a file."""
    if not path.exists():
        return 0
    content = path.read_text(encoding="utf-8", errors="ignore")
    pattern = re.compile(r"TODO|PLACEHOLDER|FIXME|XXX", re.IGNORECASE)
    return len(pattern.findall(content))


def _load_vector_knowledge(contract_name: str, contract_path: str) -> Dict[str, Any]:
    """Load vector candidates and extract knowledge for knowledge-grounded scaffolding."""
    vectors_raw = read_json(AUDIT_DIR / "vector_candidates.json")
    vectors = vectors_raw.get("vectors", []) if isinstance(vectors_raw, dict) else []

    matched_vectors: List[Dict[str, Any]] = []
    for v in vectors:
        if not isinstance(v, dict):
            continue
        seq = v.get("transaction_sequence", [])
        # Match vectors that reference this contract
        if any(str(contract_name).lower() in str(step.get("contract", "")).lower() for step in seq):
            matched_vectors.append(v)
        elif any(str(contract_path).lower() in str(step.get("contract", "")).lower() for step in seq):
            matched_vectors.append(v)
        # Also match by family if no sequence match
        elif not seq and str(contract_name).lower() in str(v.get("title", "")).lower():
            matched_vectors.append(v)

    # Sort by confidence descending, take the best one
    matched_vectors.sort(key=lambda v: float(v.get("confidence", 0.0)), reverse=True)
    best_vector = matched_vectors[0] if matched_vectors else {}

    return best_vector


def _render_assertion_stub(assertion_templates: List[Any], vector_seq: List[Dict[str, Any]], indent: str = "        ") -> str:
    """Render concrete assertion stubs from FAMILY_ASSERTIONS + vector sequence.

    Each assertion is rendered with the step's function name and precondition
    substituted in, replacing generic placeholders.
    """
    if not assertion_templates and not vector_seq:
        return ""

    lines: List[str] = []
    if assertion_templates:
        for at in assertion_templates[:3]:
            # Extract step number from assertion step field
            step_hint = str(getattr(at, "step", "")).strip()
            rendered_assertion = getattr(at, "assertion", "")
            rationale = getattr(at, "rationale", "")

            # Try to match to a sequence step
            matched_step: Optional[Dict[str, Any]] = None
            if step_hint in ("post_exploit", "final", "invariant_check"):
                matched_step = vector_seq[-1] if vector_seq else None
            elif step_hint in ("pre_exploit", "start"):
                matched_step = vector_seq[0] if vector_seq else None

            fn_name = ""
            if matched_step:
                fn_name = str(matched_step.get("function", "")).strip()

            # Build the concrete assertion
            assertion_text = rendered_assertion
            if fn_name and "<fn>" in assertion_text:
                assertion_text = assertion_text.replace("<fn>", fn_name)

            if assertion_text:
                lines.append(f"{indent}// {rationale}")
                lines.append(f"{indent}{assertion_text}")
                lines.append(f"{indent}// (replace with compile-ready assertion)")
                lines.append("")
    else:
        # Fallback: generate stubs from vector sequence
        for i, step in enumerate(vector_seq[:3], 1):
            fn = str(step.get("function", "")).strip()
            precondition = str(step.get("precondition", "")).strip()
            expected_revert = step.get("expected_revert", False)
            if expected_revert:
                lines.append(f"{indent}// Step {i} ({fn}): expect revert — {precondition}")
                lines.append(f"{indent}vm.expectRevert(); // {fn}()")
                lines.append("")
            else:
                lines.append(f"{indent}// Step {i} ({fn}): {precondition}")
                lines.append(f"{indent}// assert correctness of {fn}()")
                lines.append("")

    return "".join(lines)


def _render_exploit_body_stub(
    vector: Dict[str, Any],
    family_assertions: List[Any],
    contract_name: str,
    indent: str = "        ",
) -> str:
    """Render a knowledge-grounded exploit body from vector data and family assertions."""
    seq = vector.get("transaction_sequence", []) if isinstance(vector, dict) else []
    family = str(vector.get("family", "")) if isinstance(vector, dict) else ""

    lines: List[str] = []
    lines.append(f"{indent}// Knowledge-grounded exploit body for {family}:")
    lines.append("")

    if seq:
        for i, step in enumerate(seq, 1):
            fn = str(step.get("function", "")).strip()
            actor = str(step.get("actor", "attacker")).strip()
            contract = str(step.get("contract", contract_name)).strip()
            params = step.get("parameters", {}) if isinstance(step, dict) else {}
            precondition = str(step.get("precondition", "")).strip()
            expected_revert = step.get("expected_revert", False)

            if fn.startswith("<") or not fn:
                lines.append(f"{indent}// Step {i}: {precondition}")
                lines.append(f"{indent}// TODO: implement step {i}")
                lines.append("")
                continue

            lines.append(f"{indent}// Step {i}: {actor}.{contract}.{fn}()")
            lines.append(f"{indent}// Precondition: {precondition}")
            if expected_revert:
                lines.append(f"{indent}vm.expectRevert(); // step {i} — {fn}() should revert")
            if actor == "attacker":
                lines.append(f"{indent}vm.prank(attacker); // step {i}")
            param_str = ", ".join(f"{k}={v}" for k, v in params.items()) if params else ""
            lines.append(f"{indent}target.{fn}({param_str});")
            lines.append("")
    else:
        lines.append(f"{indent}// No transaction sequence available for {family}.")
        lines.append(f"{indent}// TODO: implement exploit based on {family} pattern")
        lines.append("")

    # Append family-specific assertion stubs
    if family_assertions:
        lines.append(f"{indent}// ── Assertion stubs from {family} template ──")
        lines.append(_render_assertion_stub(family_assertions, seq, indent))
    else:
        lines.append(f"{indent}// ── Generic assertion stubs ──")
        lines.append(_render_assertion_stub([], seq, indent))

    return "".join(lines)


