def format_comment_block(prefix: str, lines: List[str]) -> str:
    if not lines:
        return f"        // {prefix}: not determined\n"
    rendered = [f"        // {prefix}: {lines[0]}"]
    rendered.extend(f"        // {line}" for line in lines[1:])
    return "\n".join(rendered) + "\n"


def prioritize_candidates(candidates: List[str], context_lines: List[str]) -> List[str]:
    seen: List[str] = []
    for line in context_lines:
        match = re.search(r"::([A-Za-z_][A-Za-z0-9_]*)", line)
        if match:
            name = match.group(1)
            if name in candidates and name not in seen:
                seen.append(name)
    for candidate in candidates:
        if candidate not in seen:
            seen.append(candidate)
    return seen


def load_context_lines(contract_name: str, contract_path: str) -> Dict[str, List[str]]:
    contest_lines: List[str] = []
    privilege_lines: List[str] = []
    invariant_lines: List[str] = []
    rule_lines: List[str] = []
    exploit_lines: List[str] = []
    role_lines: List[str] = []
    setter_lines: List[str] = []
    sink_lines: List[str] = []
    proof_plan_lines: List[str] = []

    contest_context = read_json(AUDIT_DIR / "contest_context.json")
    if contest_context:
        if contest_context.get("scope_files"):
            contest_lines.append(f"in-scope files: {len(contest_context['scope_files'])}")
        if contest_context.get("known_issues"):
            contest_lines.append(f"known/public issues listed: {len(contest_context['known_issues'])}")
        notes = contest_context.get("contest_notes", {})
        if isinstance(notes, dict):
            contest_lines.extend(str(value) for value in notes.values())

    privilege_map = (AUDIT_DIR / "privilege_map.md").read_text(encoding="utf-8", errors="ignore") if (AUDIT_DIR / "privilege_map.md").exists() else ""
    target_file_hint = contract_path
    target_contract_hint = f"::{contract_name}"
    fallback_privilege: List[str] = []
    for line in privilege_map.splitlines():
        stripped = line.strip()
        if stripped.startswith("- "):
            entry = stripped[2:]
            if target_file_hint in entry or target_contract_hint in entry:
                privilege_lines.append(entry)
            else:
                fallback_privilege.append(entry)
        if len(privilege_lines) >= 4:
            break
    if len(privilege_lines) < 4:
        privilege_lines.extend(fallback_privilege[: 4 - len(privilege_lines)])

    invariant_map = (AUDIT_DIR / "invariant_map.md").read_text(encoding="utf-8", errors="ignore") if (AUDIT_DIR / "invariant_map.md").exists() else ""
    fallback_invariant: List[str] = []
    for line in invariant_map.splitlines():
        stripped = line.strip()
        if stripped.startswith("- "):
            entry = stripped[2:]
            if target_file_hint in entry or target_contract_hint in entry:
                invariant_lines.append(entry)
            else:
                fallback_invariant.append(entry)
        if len(invariant_lines) >= 4:
            break
    if len(invariant_lines) < 4:
        invariant_lines.extend(fallback_invariant[: 4 - len(invariant_lines)])

    rule_scan = read_json(AUDIT_DIR / "rule_scan.json")
    for item in rule_scan.get("findings", []) if isinstance(rule_scan, dict) else []:
        if not isinstance(item, dict):
            continue
        if contract_path in str(item.get("path", "")):
            rule_lines.append(f"{item.get('rule_id')}: {item.get('message')}")
        if len(rule_lines) >= 4:
            break

    exploit_rankings = (AUDIT_DIR / "exploit_rankings.md").read_text(encoding="utf-8", errors="ignore") if (AUDIT_DIR / "exploit_rankings.md").exists() else ""
    for line in exploit_rankings.splitlines():
        stripped = line.strip()
        if stripped.startswith("- ") and (contract_path in stripped or contract_name in stripped):
            exploit_lines.append(stripped[2:])
        if len(exploit_lines) >= 4:
            break

    proof_plans = read_json(AUDIT_DIR / "proof_plans.json")
    for item in proof_plans.get("proof_plans", []) if isinstance(proof_plans, dict) else []:
        if not isinstance(item, dict):
            continue
        preferred = str(item.get("preferred_test_path", ""))
        sequence = item.get("transaction_sequence", [])
        assertions = item.get("assertions", [])
        if contract_path in preferred or contract_name in str(item.get("title", "")):
            if isinstance(sequence, list):
                proof_plan_lines.extend(str(entry) for entry in sequence[:2])
            if isinstance(assertions, list):
                proof_plan_lines.extend(f"assert: {entry}" for entry in assertions[:2])
        if len(proof_plan_lines) >= 4:
            break

    contract_source = read_text_file(Path(contract_path))
    code_only = strip_solidity_comments(contract_source)
    role_lines.extend(dict.fromkeys(re.findall(r"\b([A-Z][A-Z0-9_]*_ROLE)\b", code_only)))

    functions = re.findall(r"\bfunction\s+([A-Za-z_][A-Za-z0-9_]*)\s*\(", code_only)
    setter_keywords = ("set", "update", "configure", "change", "grant", "revoke")
    sink_keywords = ("withdraw", "rescue", "claim", "mint", "burn", "transfer", "sweep", "collect", "execute")
    setter_candidates = list(dict.fromkeys(name for name in functions if name.lower().startswith(setter_keywords)))
    sink_candidates = list(dict.fromkeys(name for name in functions if any(keyword in name.lower() for keyword in sink_keywords)))
    prioritized_context = privilege_lines + invariant_lines
    setter_lines.extend(prioritize_candidates(setter_candidates, prioritized_context))
    sink_lines.extend(prioritize_candidates(sink_candidates, prioritized_context))

    return {
        "contest": contest_lines[:4],
        "privilege": privilege_lines[:4],
        "invariant": invariant_lines[:4],
        "rules": rule_lines[:4],
        "exploits": exploit_lines[:4],
        "proof_plans": proof_plan_lines[:4],
        "roles": role_lines[:4],
        "setters": setter_lines[:4],
        "sinks": sink_lines[:4],
        # Rich finding-candidate fields for knowledge-grounded stub comments
        "violated_invariant": _load_violated_invariant(contract_path),
        "blocking_assumptions": _load_blocking_assumptions(contract_path),
    }


def _load_violated_invariant(contract_path: str) -> List[str]:
    """Extract violated_invariant from the best matching finding candidate."""
    candidates = read_json(AUDIT_DIR / "finding_candidates.json")
    best: Dict[str, Any] = {}
    best_score = -1.0
    for fc in candidates.get("findings", []) if isinstance(candidates, dict) else []:
        if not isinstance(fc, dict):
            continue
        if contract_path in str(fc.get("target_contract", "")):
            score = float(fc.get("confidence_score", 0.0))
            if score > best_score:
                best_score = score
                best = fc
    invariant = str(best.get("violated_invariant", "")).strip()
    return [invariant] if invariant else []


def _load_blocking_assumptions(contract_path: str) -> List[str]:
    """Extract blocking_unknowns (treated as blocking_assumptions) from the best matching finding candidate."""
    candidates = read_json(AUDIT_DIR / "finding_candidates.json")
    best: Dict[str, Any] = {}
    best_score = -1.0
    for fc in candidates.get("findings", []) if isinstance(candidates, dict) else []:
        if not isinstance(fc, dict):
            continue
        if contract_path in str(fc.get("target_contract", "")):
            score = float(fc.get("confidence_score", 0.0))
            if score > best_score:
                best_score = score
                best = fc
    blockers = best.get("blocking_unknowns", [])
    if isinstance(blockers, list):
        return [str(b) for b in blockers[:4] if str(b).strip()]
    return []


