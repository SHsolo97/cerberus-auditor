def detect_scaffold_family() -> str:
    """Detect the preferred scaffold family from init phase details."""
    status_snapshot = read_json(STATUS_FILE)
    init_details = status_snapshot.get("init", {}).get("details", {}) if isinstance(status_snapshot, dict) else {}
    toolchain = init_details.get("toolchain_config") or {}
    if isinstance(toolchain, dict):
        family = toolchain.get("test_scaffold_family", "generic")
        if family:
            return str(family)
    # Fallback: resolve from filesystem
    tc = resolve_toolchain(Path(".").resolve())
    return tc.test_scaffold_family


def run_compile_check_generic(
    exploit_path: Path,
    scaffold_family: str,
    hardhat_bin: Optional[str] = None,
) -> tuple[bool, List[str]]:
    """
    Run a compile/syntax check for non-Foundry scaffold families.

    - foundry: uses forge test --match-path (existing behavior)
    - hardhat: runs hardhat compile
    - generic: uses solc --ast-compact-json for a basic syntax check
    """
    if scaffold_family == "foundry":
        return run_compile_check(exploit_path, Path("unused"))

    warnings: List[str] = []

    if scaffold_family == "hardhat":
        if not hardhat_bin:
            hardhat_bin = which("hardhat")
        if not hardhat_bin:
            return False, ["hardhat not available for scaffold compile check."]
        result = run_cmd([hardhat_bin, "compile"], cwd=Path("."), timeout=300)
        if result.ok:
            return True, []
        return False, [f"hardhat compile: {result.stderr[:300]}"]

    # Generic: basic solc syntax check
    solc_bin = which("solc")
    if not solc_bin:
        return False, ["no Solidity compiler found for syntax check."]
    # Try to parse the file as valid Solidity
    result = run_cmd([solc_bin, "--base-path", ".", str(exploit_path)], cwd=Path("."), timeout=60)
    if result.returncode == 0:
        return True, []
    # solc returns non-zero for warnings too — check stderr for actual errors
    stderr = result.stderr.strip()
    if not stderr:
        return True, []  # No output = no errors
    # Only fail on parse errors, not on unused variables etc.
    if "ParserError" in stderr or "SyntaxError" in stderr or "Error:" in stderr.split("\n")[0]:
        return False, [f"solc syntax error: {stderr[:300]}"]
    return True, [f"solc warnings (non-blocking): {stderr[:200]}"]


def run_compile_check(exploit_path: Path, invariant_path: Path) -> tuple[bool, List[str]]:
    forge_bin = dependency_map("forge").get("forge")
    if not forge_bin:
        return False, ["forge not available for scaffold compile check."]

    warnings: List[str] = []
    for path in (exploit_path, invariant_path):
        result = run_cmd(
            [forge_bin, "test", "--match-path", str(path), "--match-test", "^$"],
            cwd=Path("."),
            timeout=300,
        )
        if not result.ok:
            detail = result.stderr.strip() or result.stdout.strip() or f"compile check failed for {path}"
            warnings.append(detail[:280])
            return False, warnings
    return True, warnings


